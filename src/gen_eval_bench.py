def compute_hash(text: str) -> str:
    """Compute SHA-256 hash of text for contamination check."""
    return hashlib.sha256(text.encode()).hexdigest()[:16]


def build_benchmark():
    """Build full benchmark with provenance fields."""
    all_challenges = []
    category_map = {"pwn": PWN, "rev": REV, "crypto": CRYPTO, "web": WEB}

    # Add main category challenges
    for cat_name, cat_challenges in category_map.items():
        for ch in cat_challenges:
            out = dict(ch)
            out["category"] = cat_name
            out["system_prompt"] = SYS_PROMPTS[cat_name]
            out["training_overlap_hash"] = compute_hash(ch["prompt"])
            all_challenges.append(out)

    # Add new task types
    for task_list in [VULN_ID, PATCH_GEN, EXPLOIT_TRACE]:
        for ch in task_list:
            out = dict(ch)
            out["system_prompt"] = SYS_PROMPTS.get(out["category"], "You are an expert CTF player.")
            out["training_overlap_hash"] = compute_hash(ch["prompt"])
            all_challenges.append(out)

    return all_challenges


def main():
    parser = argparse.ArgumentParser(description="Generate CTF evaluation benchmark")
    parser.add_argument("--output", default=None,
                        help="Output path (default: data/eval/ctf_bench.jsonl)")
    args = parser.parse_args()

    if args.output:
        output_path = Path(args.output)
    else:
        output_path = Path(__file__).parent.parent / "data" / "eval" / "ctf_bench.jsonl"
    output_path.parent.mkdir(parents=True, exist_ok=True)

    challenges = build_benchmark()

    with open(output_path, "w") as f:
        for ch in challenges:
            f.write(json.dumps(ch) + "\n")
    print(f"Wrote {len(challenges)} challenges to {output_path}")

    # Stats
    from collections import Counter
    cats = Counter(c["category"] for c in challenges)
    diffs = Counter(c["difficulty"] for c in challenges)
    types = Counter(c["task_type"] for c in challenges)
    prompts = len(set(c["system_prompt"] for c in challenges))
    print(f"  Categories: {dict(cats)}")
    print(f"  Difficulties: {dict(diffs)}")
    print(f"  Task types: {dict(types)}")
    print(f"  Unique system prompts: {prompts}")
    print(f"  Contamination hashes: computed")


if __name__ == "__main__":
    main()
