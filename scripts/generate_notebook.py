        "",
        "# ============================================================",
        "# 3.1 Load config + apply mode overrides",
        "# ============================================================",
        "import yaml",
        'with open(f"/content/configs/{MODEL}.yaml") as f:',
        "    cfg = yaml.safe_load(f)",
        "mc = cfg.get(\"model\", cfg)",
        "tc = cfg.get(\"training\", {})",
        "",
        'MODEL_NAME = mc["name"]',
